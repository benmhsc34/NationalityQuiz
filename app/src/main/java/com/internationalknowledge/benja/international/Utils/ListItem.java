package com.internationalknowledge.benja.international.Utils;

public class ListItem {

    private int flag;
    private String name;

    public ListItem(int flag, String name) {
        this.flag = flag;
        this.name = name;
    }

    public int getFlag() {
        return flag;
    }

    public String getName() {
        return name;
    }
}
