package com.internationalknowledgequiz.benja.international.Utils;

public class ListItem {

    private String flag;
    private String name;

    public ListItem(String flag, String name) {
        this.flag = flag;
        this.name = name;
    }

    public String getFlag() {
        return flag;
    }

    public String getName() {
        return name;
    }

}
